  public createEntitiesForLevel(level: number[][]): Entity[] {
    const entities: Entity[] = [];

    // Mappning av siffror till entiteter
    const ENTITY_MAP: { [key: number]: (x: number, y: number) => Entity } = {
      1: (x, y) => new Block(x, y),
      2: (x, y) => new Star(x, y),
      3: (x, y) => new Heart(x, y),
      4: (x, y) => new Plant(x, y),
      5: (x, y) => new Ghost(x, y),
      6: (x, y) => new TetrisBlock(x, y),
      7: (x, y) => new WinBlock(x, y),
    };

    // loopa ver level och skapa alla entiteter
    for (let row = 0; row < level.length; row++) {
      for (let col = 0; col < level[row].length; col++) {
        const entityType = level[row][col];
        const x = col * this.gridSize + this.gridSize / 2;
        const y = row * this.gridSize + this.gridSize / 2;

        // Skapa entitet om den finns i mappningen
        if (ENTITY_MAP[entityType]) {
          entities.push(ENTITY_MAP[entityType](x, y));
        }
      }
    }

    return entities;
  }
